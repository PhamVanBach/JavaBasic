    public static boolean isNumeric(String strNum) {
        if (strNum == null) {
            return false;
        }
        try {
            Integer.parseInt(strNum);
            return true;
        }
        catch (NumberFormatException e) {
             return false;
        }
    }
    
    private void jbtnBoldSoChanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBoldSoChanActionPerformed
        ArrayList<Integer> listIndex = new ArrayList<>();
        for(int i = 0; i < jListNumber.getModel().getSize(); i++){
            int numOnList = Integer.parseInt(jListNumber.getModel().getElementAt(i));
            if(numOnList % 2 == 0){
//                
                listIndex.add(i);
            }
        int[] listIndexInt = listIndex.stream().mapToInt(Integer::intValue).toArray();
        jListNumber.setSelectedIndices(listIndexInt);
        }                                              
    }//GEN-LAST:event_jbtnBoldSoChanActionPerformed

    private void jbtnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnInputActionPerformed
        // TODO add your handling code here:
        jListNumber.setModel(dm);
        jListNumber.getSelectionModel().setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
        if(isNumeric(jTxtInput.getText())){    
            if(jTxtInput.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Vui long khong bo trong");
            }
            
            if(!jCkbNhapSoAm.isSelected()){
                int numInput = Integer.parseInt(jTxtInput.getText());
                if(numInput < 0){
                    JOptionPane.showMessageDialog(this, "Vui long nhap so > 0");
                    return;
                }
            }
            if(jCkbNhapSoAm.isSelected()){
                dm.addElement(jTxtInput.getText());
            }
            else{
                dm.addElement(jTxtInput.getText());
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "vui long nhap so!");
        }
        
        jTxtInput.setText("");
    }//GEN-LAST:event_jbtnInputActionPerformed

